import React from 'react';
import { useNavigate, useLocation } from 'react-router-dom';
import {
  Drawer,
  List,
  ListItem,
  ListItemButton,
  ListItemIcon,
  ListItemText,
  Toolbar,
} from '@mui/material';
import {
  Dashboard as DashboardIcon,
  PlaylistPlay as ProcessIcon,
  Computer as SystemIcon,
  Warning as AlertIcon,
  Description as LogIcon,
  AutoFixHigh as WizardIcon,
} from '@mui/icons-material';

const drawerWidth = 240;

const menuItems = [
  { text: 'Dashboard', icon: <DashboardIcon />, path: '/' },
  { text: 'Processes', icon: <ProcessIcon />, path: '/processes' },
  { text: 'App Wizard', icon: <WizardIcon />, path: '/wizard' },
  { text: 'System', icon: <SystemIcon />, path: '/system' },
  { text: 'Alerts', icon: <AlertIcon />, path: '/alerts' },
  { text: 'Logs', icon: <LogIcon />, path: '/logs' },
];

function Navigation() {
  const navigate = useNavigate();
  const location = useLocation();

  return (
    <Drawer
      variant="permanent"
      sx={{
        width: drawerWidth,
        flexShrink: 0,
        [`& .MuiDrawer-paper`]: { width: drawerWidth, boxSizing: 'border-box' },
      }}
    >
      <Toolbar />
      <List>
        {menuItems.map((item) => (
          <ListItem key={item.text} disablePadding>
            <ListItemButton
              selected={location.pathname === item.path}
              onClick={() => navigate(item.path)}
            >
              <ListItemIcon>{item.icon}</ListItemIcon>
              <ListItemText primary={item.text} />
            </ListItemButton>
          </ListItem>
        ))}
      </List>
    </Drawer>
  );
}

export default Navigation;